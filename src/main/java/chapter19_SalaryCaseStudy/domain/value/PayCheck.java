package chapter19_SalaryCaseStudy.domain.value;


public class PayCheck {
	private Money amount;
	private DateOnly payDate;

	public PayCheck(Money amount, DateOnly payDate) {
		this.amount = amount;
		this.payDate = payDate;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((amount == null) ? 0 : amount.hashCode());
		result = prime * result + ((payDate == null) ? 0 : payDate.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PayCheck other = (PayCheck) obj;
		if (amount == null) {
			if (other.amount != null)
				return false;
		} else if (!amount.equals(other.amount))
			return false;
		if (payDate == null) {
			if (other.payDate != null)
				return false;
		} else if (!payDate.equals(other.payDate))
			return false;
		return true;
	}
}
